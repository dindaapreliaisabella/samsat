      <h1><b>Latar Belakang</h1></b>
      <img class="imgr borderedbox inspace-5" src="../images/demo/imgr.gif" alt="">
      <p>Badan Pendapatan Provinsi tugas melaksanakan tugas melaksanakan fungsi pemerintahan dibidang Pajak Daerah, Bidang Pimpinan Bukan Pajak Daerah, perencanaan, pembinaan dan pengawasan pendapatan yang menjadi kewenangan daerah dan tugas pembantuan yang ditugaskan Daerah Provinsi.  Sedangkan Fungsi Badan Pendapatan Daerah meliputi: </p>
      <p>a.  Perumusan kebijakan teknis di bidang pendapatan daerah sesuai dengan rencana strategis yang ditetapkan pemerintah daerah</p>
      <p>b.  Perencanaan, pembinaan kebijakan teknis, perencanaan dan pengembangan sistem informasi pendapatan</p>
      <p>c.  Perencanaan, pembinaan kebijakan teknis di bidang pajak daerah</p>
      <p>d.  Perencanaan, pembinaan kebijakan pajak penerimaan bukan daerah</p>
      <p>e.  Perencanaan, pembinaan kebijakan teknis dan evaluasi pendapatan</p>
      <p>f.  Penyelenggaraan urusan kesekretariatan</p>
      <p>g.  Unit Pelaksanaan Pelaksana Teknis Badan</p>
      <p>h.  Pembinaan kelompok Jabatan Fungsional</p>
      <p>i.  Pelaksanaan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugasnya.  Dalam sejarah pemerintahan daerah di Indonesia, sejak Indonesia merdeka sampai saat ini pajak dan retribusi daerah telah menjadi sumber penerimaan yang dapat diandalkan bagi daerah.  Sejak tahun 1948 berbagai Undang-Undang tentang Pemerintahan Daerah dan perimbangan keuangan antara pusat dan daerah telah menempatkan pajak dan retribusi daerah sebagai sumber penerimaan daerah, bahkan, dalam Undang-Undang Nomor 5 Tahun 1947 pajak dan retribusi daerah menjadi pendapatan daerah. </p>
      <p>Pajak sebagai satu perwujudan kewajiban kenegaraan, ditegaskan bahwa penempatan beban kepada rakyat seperti pajak, retribusi dan lain-lain, harus ditetapkan dengan Undang-Undang.  Pendapatan Asli Daerah (PAD) yang antara lain berupa Pajak Daerah dan Retribusi Daerah, menjadi salah satu sumber pembiayaan dalam penyelenggaraan pemerintahan dan pembangunan daerah dalam rangka meningkatkan dan memeratakan kesejahteraan masyarakat.  Dengan demikian daerah mampu melaksanakan otonomi, yaitu menyediakan dan mengatur rumah tangganya sendiri. </p>
      <p>Dengan dikeluarkannya Undang-Undang Nomor 34 Tahun 2000 tentang Perubahan atas Undang-Undang Republik Indonesia Nomor 18 Tahun 1997 tentang Pajak Daerah dan Retribusi Daerah, yang berisi ketentuan-ketentuan pokok yang memberikan kebijakan baru dan arahan bagi daerah dalam pelaksanaan pemungutan pajak daerah dan retribusi daerah  penetapan umum untuk menjamin penetapan daerah dan retribusi daerah.</p>
      <p>Pajak daerah terdiri dari berbagai jenis pajak yang terkait dengan berbagal sendi kehidupan masyarakat.  Demikian pula dengan retribusi daerah.  Masing-masing jenis pajak dan retribusi daerah memiliki objek, subjek, tarif, dan berbagai ketentuan pengenaan yang berbeda dengan jenis pajak atau retribusi daerah lainnya.  Di sisi lain, semangat otonomi daerah yang diberlakukan di Indonesia memungkinkan setiap daerah provinsi atau kabupaten / kota pembantuannya sendiri, termasuk dalam bidang pajak dan retribusi daerah.</p>
      <p>Pajak Daerah dan Retribusi Daerah merupakan salah satu sumber pendapatan yang sangat penting guna membiayai penyelenggaraan pemerintahan daerah dan pembangunan daerah untuk memantapkan otonomi daerah yang luas, nyata dan bertanggung jawab.  Berdasarkan Undang-Undang Nomor 32 Tahun 2004 tentang kewenangan Pemerintah Provinsi sebagal Daerah Otonom, maka ditetapkan Peraturan Daerah Provinsi Kalimantan Timur Nomor: 8 Tahun 2014 Tentang Perubahan Atas Peraturan Daerah Provinsi Kalimantan Timur Nomor 01 Tahun 2011 Tentang Pajak Daerah Provinsi kalimantan Timur, Peraturan Daerah Provinsi  Kalimantan Timur Nomor: 1 Tahun 2012 Tentang Retribusi Jasa Umum Peraturan Daerah Provinsi Kalimantan Timur Nomor: 2 Tahun 2012 Tentang Retribusi Jasa Usaha Peraturan Daerah Provinsi Kalimantan Timur Nomor: 3 Tahun 2012 Tentang Retribusi Perizinan Tertentu Peraturan Daerah Provinsi Kalimantan Timur Nomor 15 Tahun 2012 Tentang Layanan  Informasi Publik Dilingkungan Pemerintah Provinsi Kalimantan Timur, tentang jenis Pajak Daerah yang terdiri sebagai berikut:</p>
      <p>1. Pajak Kendaraan Bermotor</p>
      <p>2. Bea Balik Nama Kendaraan Bermotor</p>
      <p>3. Permukaan Udara Pajak.</p>
      <p>4. Pajak Bahan Bakar Kendaraan Bermotor</p>
      <p>5. Pajak Rokok</p>
      <p>Dari jenis-jenis Pajak Daerah memberikan kontribusi cukup besar dalam rangka meningkatankan Pendapatan Asli tersebut diatas, penerimaan yang diterima Daerah (PAD) Kalimantan Timur adalah jenis pemungutan Pajak Kendaraan Bermotor.  Berdasarkan pernyataan dari Badan Pendapatan Daerah Provinsi Kalimantan Timur tentang Realisasi pendapatan dari PKB dan BBNKB di Kaltim.</p>
      <p>Hal tersebut memberikan kontribusi yang cukup besar untuk Pendapatan Asli Daerah (PAD) Kaliman Timur dari penerimaan Pajak Kendaraan Bermotor dan lain-lain.  oleh sebab itu perlu dibentuk UPTD Pelayanan Pajak dan Retribusi Daerah Wilayah Paser BAPENDA Prov.  Kaltim untuk membantu meningkatan Pendapatan Asli Daerah (PAD) dan meningkatkan pelayanan terhadap masyarakat. </p>
      <h1><b>Tujuan</h1></b>
      <p>Tujuan Pembentukan UPTD Pelayanan Pajak dan Retribusi Daerah Wilayah Paser ditetapkan sebagai berikut:</p>
      <p>1. Meningkatkan Kualitas Pelayanan Kepada Masyarakat.</p>
      <p>2. Meningkatkan Sumber Daya Manusia. </p>
      <p>3. Meningkatkan Identifikasi dan Kemanan Kepemilikan Kendaraan Bermotor. </p>
      <p>4. Meningkatkan Penerimaan Daerah. </p>
